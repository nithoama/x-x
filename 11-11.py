import math
n=float(input("Digite o âgulo que voçê deseja "))
b=math.sin(math.radians(n))
print(f"O seno de {n} e {b:.2f}")
c=math.cos(math.radians(n))
print(f"O cosseno de {n} e {c:.2f}")
d=math.tan(math.radians(n))
print(f"O tangente de de {n} e {d:.2f}")
